# O número que é armazenado no contador informa de onde o while
# deve começar a contar.
contador = 0

while contador <= 100:
    print(contador)
    contador += 1

# Para que o while execute o valor limite, é necessário
# colocar o sinal de <= para que ele execute a contagem
# do valor limite.
