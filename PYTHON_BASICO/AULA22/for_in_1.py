"""
For in em Python
Iterando strings com for
Função range (start=0, stop, step=1)
"""

for n in range(10):
    print(n)

# A função 'range' tem três argumentos 'start', 'stop' e 'step'
# 'start' serve para determinar o valor inicial, vem por padrão com
# valor inicial de zero.
# O argumento 'stop' serve para determinar o valor final ou limite
# 'step' faz referência de quantas em quantas casas ele vai pular, vem por
# padrão com o valor de 1

