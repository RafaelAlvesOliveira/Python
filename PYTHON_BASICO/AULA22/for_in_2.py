"""
For in em Python
Iterando strings com for
Função range (start=0, stop, step=1)
"""

for n in range(20, 10, -1):
    print(n)

# Para reverter essa contagem o 'step' deve ser -1 para decrementar.
# A iteração sempre para um número antes do valor informado no
# argumento 'stop', então sempre se deve acrescentar um número a menos
# ou a mais do valor que se deseja.
