"""
For / Else em Python
"""

variavel = ['Rafael', 'Luiz', 'Otávio', 'Maria']

for valor in variavel:
    print(valor)
    continue
    print(valor)

for valor in variavel:
    print(valor)
    break
    print(valor)

# o comando 'continue' serve para quando uma determinada
# condição é feita e após isso o laço é encerrado
# No caso do comando break, após a primeira volta do laço
# ele já é quebrado e segue para a próxima função do código.
