"""
Operadores Relacionais
== igualdade
> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
!= diferente
"""

num_1 = 2  # int
num_2 = 2  # int

expressao = (num_1 == num_2)
print(expressao)
# == serve para checar se o valores são iguais e o mesmo tipo

expressao_1 = (num_1 > num_2)
print(expressao_1)
# > serve para checar se a variável da esquerda é maior que a variável da direita

expressao_2 = (num_1 < num_2)
print(expressao_2)
# > serve para checar se a variável da esquerda é menor que a variável da direita

expressao_3 = (num_1 >= num_2)
print(expressao_3)
# >= serve para checar se a varíavel da esquerda é maior ou igual a variável da direita

expressao_4 = (num_1 <= num_2)
print(expressao_4)
# <= serve para checar se a variável da esquerda é menor ou igual a variável da direita

var_1 = 'Rafael'
var_2 = 'Rafa'
expressao_5 = (var_1 != var_2)
print(expressao_5)
# != serve para checar se a variável da esquerda é diferente da variável da direita




