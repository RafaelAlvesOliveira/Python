# elif é else_if serve para checar vários valores ao mesmo tempo.
# Quando todas as expressões forem falsas o else será executado.
# Se todas as expressões forem falsas, nada será executado.

if False:
    print("Verdaeiro.")
elif False:
    print("Agora é verdadeiro.")
elif False:
    print("Mais uma verdadeira.")
else:
    print("Não é verdadeiro.")

