""""
Tipos de dados
str - string - textos 'Assim' "Assim"
int - inteiro - 123456 0 -10 -20 -50 -60 -15000 1500
float - real/ponto flutuante - 10.50 1.5 -10.10 -50.93 0.0
bool - booleano/lógico - True/False 10 == 10
"""

# Operações aritméticas podem ser feitas dentro do comando print
print(10 + 10)

# Mas se for feita a união de duas strings, elas serão concatenadas
print('10' + '10')

# Se isso for feito com uma string e um número ele apresentará um
# erro informando que não pode realizar essa operação.
print('10' + 10)
