""""
str - string
"""
# A barra invertida é lida pelo interpretador do python.
print('Esse é meu \n (str).')
# O caractere \n vai gerar uma quebra de linha.

print(r'Esse é meu \n (str).')
# Quando se adiciona o caractere 'r' para que o interpretador
# entenda que não deve executar nada dentro do print

print(r'Esse é meu \'Aspas aqui\' (str).')
# Nesse caso as aspas internas irão aparecer nesse caso, devido ao
# caractere 'r' no início da string.



