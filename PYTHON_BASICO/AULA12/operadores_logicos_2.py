# O operador lógico "in" serve para verificar se um determinado parâmetro
# existe no valor contido numa determinada variável.

nome = 'Rafael Alves'

if 'f' in nome:
    print("Existe a letra F no seu nome.")

if 'fael' in nome:
    print("Existe a letra Fael no seu nome.")
else:
    print("Não existe.")

if 'dad' in nome:
    print("Existe a letra Fael no seu nome.")
else:
    print("Não existe.")
