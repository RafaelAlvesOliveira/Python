# Divisão inteira, retorna o valor inteiro
# e ignora a parte decimal do número
print(10.5 // 3)
# Não tem problema fazer cálculos usando
# números flutuantes e inteiros
print(10 // 3)

# Divisão, retorna uma valor flutuante e não faz o arredondamento
# de valores para números inteiros
print(10 / 3)

# Potenciação
print(2 ** 10)
# Retorna um número elevado a uma determinado expoente

# Módulo da divisão, retorna o resto da divisão
print(10 % 5)
print(10 % 3)

