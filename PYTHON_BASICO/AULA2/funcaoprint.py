# Por padrão o print separa os argumentos por padrão
print('Rafael', 'Alves', 'Nascimento')
# A função print irá usar um argumento nomeado o sep para separar as palavras com um '-'
print('Rafael', 'Alves', sep='-', end='')
# a função print também pode usar o argumento nomeado end para que os argumentos dentro
# da função sejam contínuos sem quebra de linha
print('João', 'e', 'Maria', sep='-', end='')
# a linguagem python diferencia minúsculas de maiscúlas
