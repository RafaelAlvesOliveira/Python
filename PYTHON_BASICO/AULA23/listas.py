# Uma lista pode conter muitos valores em um 'array'
# ela é como uma variável, mas suporta diversos 'tipos'
# de valores, independente do 'tipo'

#        0   1   2    3    4
lista = ['A', 'Bacana', "C", 'D', 'E', 10.5]
#   -    5   4   3    2    1

# Os itens na lista podem ser acessados pelos índices
# são similares as 'strings'

string = 'ABCDE'

print(string[1])
# Uma 'string' só suporta um valor, uma lista suporta muitos
# valores e de vários 'tipos'

print(lista[6])
# Se não for informado nenhum valor no print, será impressa
# toda a lista
