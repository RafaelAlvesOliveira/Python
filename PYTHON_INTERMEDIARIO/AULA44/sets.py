# Conjuntos são similares as listas e dicionários
# É um conjunto de elementos que podem ser adicionados
# dentro de uma mesma estrutra de dados. A diferença entre
# sets, listas e dicionários é que eles só suportam elementos
# únicos.

s1 = {1, 2, 3, 4, 5, 6}

# Os 'sets' não tem pares de chave e valor, só tem
# valores, seria como se estivesse a criar uma lista,
# porém utilizando chaves. Os 'sets' só suportam
# elementos imutáveis, números, strings, tuplas.
# Visto os 'sets' não terem índice não é possível
# acessar um valor em específico diretamente dentro dele.
print(s1)

